// 15. Calculate the slope, x-intercept and y-intercept of y = 2x -2

// For y=mx+c :
// Slope = m
// x-intercept = -c/m
// y-intercept = c

const m = 2;
const c = -2;

const xIntercept = -c / m;
const yIntercept = c;

console.log(`Slope (m): ${m}`);
console.log(`x-intercept: (${xIntercept}, 0)`);
console.log(`y-intercept: (0, ${yIntercept})`);
